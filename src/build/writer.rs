use std::{
    collections::BTreeSet,
    fs::{File, OpenOptions, create_dir_all},
    io::{BufReader, BufWriter, Read, Seek, Write},
    path::Path,
};

use camino::{Utf8Path, Utf8PathBuf};
use compress_tools::{ArchiveContents, ArchiveIterator};
use zip::{ZipWriter, result::ZipError, write::SimpleFileOptions};

use crate::domain::{BuildPlan, SourceFile, SourceKind, ZipCompression, ZipEntrySpec};

pub fn write_plan(plan: &BuildPlan, destination: &Utf8Path) -> crate::Result<Vec<Utf8PathBuf>> {
    write_plan_with_compression(plan, destination, ZipCompression::Deflate)
}

pub fn write_plan_with_compression(
    plan: &BuildPlan,
    destination: &Utf8Path,
    compression: ZipCompression,
) -> crate::Result<Vec<Utf8PathBuf>> {
    if !plan.writes_files() {
        return Ok(Vec::new());
    }

    validate_plan_paths(plan)?;
    let options = file_options(compression);
    create_dir_all(destination)?;
    let mut written_paths = Vec::with_capacity(plan.zips.len());
    plan.zips.iter().try_for_each(|zip_spec| {
        let zip_path = destination.join(&zip_spec.file_name);
        let mut writer = open_destination_zip(&zip_path)?;
        zip_spec.entries.iter().try_for_each(|entry| {
            write_entry(entry, &mut writer, options)?;
            Ok::<_, crate::Error>(())
        })?;
        writer.finish()?;
        written_paths.push(zip_path);
        Ok::<_, crate::Error>(())
    })?;
    Ok(written_paths)
}

fn file_options(compression: ZipCompression) -> SimpleFileOptions {
    let method = match compression {
        ZipCompression::Deflate => zip::CompressionMethod::Deflated,
        ZipCompression::Store => zip::CompressionMethod::Stored,
    };
    SimpleFileOptions::default().compression_method(method)
}

fn validate_plan_paths(plan: &BuildPlan) -> crate::Result<()> {
    let mut output_file_names = BTreeSet::new();
    plan.zips.iter().try_for_each(|zip_spec| {
        validate_output_file_name(&zip_spec.file_name)?;
        if !output_file_names.insert(zip_spec.file_name.as_str()) {
            return Err(crate::Error::InvalidPath(format!(
                "duplicate output zip file name: {}",
                zip_spec.file_name
            )));
        }

        let mut entry_names = BTreeSet::new();
        zip_spec.entries.iter().try_for_each(|entry| {
            validate_zip_entry_name(&entry.output_name)?;
            if !entry_names.insert(entry.output_name.as_str()) {
                return Err(crate::Error::InvalidPath(format!(
                    "duplicate zip entry name in {}: {}",
                    zip_spec.file_name, entry.output_name
                )));
            }
            Ok(())
        })
    })?;
    Ok(())
}

fn validate_output_file_name(name: &str) -> crate::Result<()> {
    if is_normal_relative_component(name) {
        Ok(())
    } else {
        Err(crate::Error::InvalidPath(format!(
            "unsafe output zip file name: {name}"
        )))
    }
}

fn validate_zip_entry_name(name: &str) -> crate::Result<()> {
    if name.split('/').all(is_normal_relative_component) {
        Ok(())
    } else {
        Err(crate::Error::InvalidPath(format!(
            "unsafe zip entry name: {name}"
        )))
    }
}

fn is_normal_relative_component(component: &str) -> bool {
    !component.is_empty()
        && component != "."
        && component != ".."
        && !component.contains('/')
        && !component.contains('\\')
        && !component.contains('\0')
        && !has_windows_drive_prefix(component)
}

fn has_windows_drive_prefix(path: &str) -> bool {
    let bytes = path.as_bytes();
    bytes.len() >= 2 && bytes[0].is_ascii_alphabetic() && bytes[1] == b':'
}

fn open_destination_zip(zip_file_path: &Utf8Path) -> crate::Result<ZipWriter<BufWriter<File>>> {
    let zip_file = OpenOptions::new()
        .create(true)
        .truncate(true)
        .write(true)
        .open(zip_file_path)?;
    let writer = BufWriter::new(zip_file);
    let mut zip_writer = ZipWriter::new(writer);
    zip_writer.set_comment("Generated by mame_coalesce")?;
    Ok(zip_writer)
}

fn write_entry(
    entry: &ZipEntrySpec,
    zip_writer: &mut ZipWriter<BufWriter<File>>,
    options: SimpleFileOptions,
) -> crate::Result<()> {
    match entry.source.kind {
        SourceKind::BareFile => {
            copy_bare_file(&entry.source, &entry.output_name, zip_writer, options)
        }
        SourceKind::ZipEntry => {
            copy_from_zip_entry(&entry.source, &entry.output_name, zip_writer, options)
        }
        SourceKind::ArchiveEntry => {
            copy_from_generic_archive(&entry.source, &entry.output_name, zip_writer, options)
        }
    }
}

fn copy_bare_file(
    source: &SourceFile,
    destination_name: &str,
    zip_writer: &mut ZipWriter<BufWriter<File>>,
    options: SimpleFileOptions,
) -> crate::Result<()> {
    let input_file = File::open(&source.canonical_path)?;
    let mut input_reader = BufReader::new(input_file);
    zip_writer.start_file(destination_name, options)?;
    std::io::copy(&mut input_reader, zip_writer)?;
    Ok(())
}

fn copy_from_zip_entry(
    source: &SourceFile,
    destination_name: &str,
    zip_writer: &mut ZipWriter<BufWriter<File>>,
    options: SimpleFileOptions,
) -> crate::Result<()> {
    let entry_name = source.entry_name.as_deref().ok_or_else(|| {
        crate::Error::InvalidPath(format!(
            "archive source has no entry name: {}",
            source.display_name()
        ))
    })?;
    let input_file = File::open(&source.canonical_path)?;
    let input_reader = BufReader::new(input_file);
    let mut archive = zip::ZipArchive::new(input_reader)?;

    if copy_zip_entry_by_enclosed_name(
        &mut archive,
        entry_name,
        destination_name,
        zip_writer,
        options,
    )? {
        Ok(())
    } else {
        Err(crate::Error::InvalidPath(format!(
            "archive entry not found: {}",
            source.display_name()
        )))
    }
}

fn copy_zip_entry_by_enclosed_name<R: Read + Seek>(
    archive: &mut zip::ZipArchive<R>,
    entry_name: &str,
    destination_name: &str,
    zip_writer: &mut ZipWriter<BufWriter<File>>,
    options: SimpleFileOptions,
) -> crate::Result<bool> {
    let requested_path = Path::new(entry_name);

    match archive.by_name(entry_name) {
        Ok(mut file) => {
            if zip_entry_enclosed_name_matches(&file, requested_path)? {
                zip_writer.start_file(destination_name, options)?;
                std::io::copy(&mut file, zip_writer)?;
                return Ok(true);
            }
        }
        Err(ZipError::FileNotFound) => {}
        Err(error) => return Err(error.into()),
    }

    for index in 0..archive.len() {
        let mut file = archive.by_index(index)?;
        if file.is_dir() {
            continue;
        }
        if zip_entry_enclosed_name_matches(&file, requested_path)? {
            zip_writer.start_file(destination_name, options)?;
            std::io::copy(&mut file, zip_writer)?;
            return Ok(true);
        }
    }

    Ok(false)
}

fn zip_entry_enclosed_name_matches<R: Read>(
    file: &zip::read::ZipFile<'_, R>,
    requested_path: &Path,
) -> crate::Result<bool> {
    file.enclosed_name().map_or_else(
        || {
            Err(crate::Error::InvalidPath(format!(
                "unsafe name inside zip: {}",
                file.name()
            )))
        },
        |enclosed_name| Ok(enclosed_name == requested_path),
    )
}

fn copy_from_generic_archive(
    source: &SourceFile,
    destination_name: &str,
    zip_writer: &mut ZipWriter<BufWriter<File>>,
    options: SimpleFileOptions,
) -> crate::Result<()> {
    let entry_name = source.entry_name.as_deref().ok_or_else(|| {
        crate::Error::InvalidPath(format!(
            "archive source has no entry name: {}",
            source.display_name()
        ))
    })?;
    let input_file = File::open(&source.canonical_path)?;
    let input_reader = BufReader::new(input_file);
    let mut iter = ArchiveIterator::from_read(input_reader)?;
    let mut copying = false;
    let mut found = false;

    for content in &mut iter {
        match content {
            ArchiveContents::StartOfEntry(name, _) => {
                copying = name == entry_name;
                if copying {
                    found = true;
                    zip_writer.start_file(destination_name, options)?;
                }
            }
            ArchiveContents::DataChunk(chunk) => {
                if copying {
                    zip_writer.write_all(&chunk)?;
                }
            }
            ArchiveContents::EndOfEntry => {
                if copying {
                    zip_writer.flush()?;
                }
                copying = false;
            }
            ArchiveContents::Err(error) => return Err(error.into()),
        }
    }

    if found {
        Ok(())
    } else {
        Err(crate::Error::InvalidPath(format!(
            "archive entry not found: {}",
            source.display_name()
        )))
    }
}

