use std::{
    collections::BTreeSet,
    fs::{File, OpenOptions, create_dir_all},
    io::{BufReader, BufWriter, Write},
};

use camino::{Utf8Path, Utf8PathBuf};
use compress_tools::{ArchiveContents, ArchiveIterator};
use zip::{ZipWriter, write::SimpleFileOptions};

use crate::domain::{BuildPlan, SourceFile, SourceKind, ZipEntrySpec};

pub fn write_plan(plan: &BuildPlan, destination: &Utf8Path) -> crate::Result<Vec<Utf8PathBuf>> {
    if !plan.writes_files() {
        return Ok(Vec::new());
    }

    validate_plan_paths(plan)?;
    create_dir_all(destination)?;
    plan.zips
        .iter()
        .map(|zip_spec| {
            let zip_path = destination.join(&zip_spec.file_name);
            let (mut writer, options) = open_destination_zip(&zip_path)?;
            for entry in &zip_spec.entries {
                write_entry(entry, &mut writer, options)?;
            }
            writer.finish()?;
            Ok(zip_path)
        })
        .collect()
}

fn validate_plan_paths(plan: &BuildPlan) -> crate::Result<()> {
    let mut output_file_names = BTreeSet::new();
    for zip_spec in &plan.zips {
        validate_output_file_name(&zip_spec.file_name)?;
        if !output_file_names.insert(zip_spec.file_name.as_str()) {
            return Err(crate::Error::InvalidPath(format!(
                "duplicate output zip file name: {}",
                zip_spec.file_name
            )));
        }

        let mut entry_names = BTreeSet::new();
        for entry in &zip_spec.entries {
            validate_zip_entry_name(&entry.output_name)?;
            if !entry_names.insert(entry.output_name.as_str()) {
                return Err(crate::Error::InvalidPath(format!(
                    "duplicate zip entry name in {}: {}",
                    zip_spec.file_name, entry.output_name
                )));
            }
        }
    }
    Ok(())
}

fn validate_output_file_name(name: &str) -> crate::Result<()> {
    if is_normal_relative_component(name) {
        Ok(())
    } else {
        Err(crate::Error::InvalidPath(format!(
            "unsafe output zip file name: {name}"
        )))
    }
}

fn validate_zip_entry_name(name: &str) -> crate::Result<()> {
    if name.split('/').all(is_normal_relative_component) {
        Ok(())
    } else {
        Err(crate::Error::InvalidPath(format!(
            "unsafe zip entry name: {name}"
        )))
    }
}

fn is_normal_relative_component(component: &str) -> bool {
    !component.is_empty()
        && component != "."
        && component != ".."
        && !component.contains('/')
        && !component.contains('\\')
        && !component.contains('\0')
        && !has_windows_drive_prefix(component)
}

fn has_windows_drive_prefix(path: &str) -> bool {
    let bytes = path.as_bytes();
    bytes.len() >= 2 && bytes[0].is_ascii_alphabetic() && bytes[1] == b':'
}

fn open_destination_zip(
    zip_file_path: &Utf8Path,
) -> crate::Result<(ZipWriter<BufWriter<File>>, SimpleFileOptions)> {
    let zip_file = OpenOptions::new()
        .create(true)
        .truncate(true)
        .write(true)
        .open(zip_file_path)?;
    let writer = BufWriter::new(zip_file);
    let mut zip_writer = ZipWriter::new(writer);
    zip_writer.set_comment("Generated by mame_coalesce")?;
    let options = SimpleFileOptions::default().compression_method(zip::CompressionMethod::Deflated);
    Ok((zip_writer, options))
}

fn write_entry(
    entry: &ZipEntrySpec,
    zip_writer: &mut ZipWriter<BufWriter<File>>,
    options: SimpleFileOptions,
) -> crate::Result<()> {
    match entry.source.kind {
        SourceKind::BareFile => {
            copy_bare_file(&entry.source, &entry.output_name, zip_writer, options)
        }
        SourceKind::ZipEntry | SourceKind::ArchiveEntry => {
            copy_from_archive(&entry.source, &entry.output_name, zip_writer, options)
        }
    }
}

fn copy_bare_file(
    source: &SourceFile,
    destination_name: &str,
    zip_writer: &mut ZipWriter<BufWriter<File>>,
    options: SimpleFileOptions,
) -> crate::Result<()> {
    let input_file = File::open(&source.canonical_path)?;
    let mut input_reader = BufReader::new(input_file);
    zip_writer.start_file(destination_name, options)?;
    std::io::copy(&mut input_reader, zip_writer)?;
    Ok(())
}

fn copy_from_archive(
    source: &SourceFile,
    destination_name: &str,
    zip_writer: &mut ZipWriter<BufWriter<File>>,
    options: SimpleFileOptions,
) -> crate::Result<()> {
    let entry_name = source.entry_name.as_deref().ok_or_else(|| {
        crate::Error::InvalidPath(format!(
            "archive source has no entry name: {}",
            source.display_name()
        ))
    })?;
    let input_file = File::open(&source.canonical_path)?;
    let input_reader = BufReader::new(input_file);
    let mut iter = ArchiveIterator::from_read(input_reader)?;
    let mut current_name = String::new();
    let mut found = false;

    for content in &mut iter {
        match content {
            ArchiveContents::StartOfEntry(name, _) => {
                current_name = name;
                if current_name == entry_name {
                    found = true;
                    zip_writer.start_file(destination_name, options)?;
                }
            }
            ArchiveContents::DataChunk(chunk) => {
                if current_name == entry_name {
                    zip_writer.write_all(&chunk)?;
                }
            }
            ArchiveContents::EndOfEntry => {
                if current_name == entry_name {
                    zip_writer.flush()?;
                }
            }
            ArchiveContents::Err(error) => return Err(error.into()),
        }
    }

    if found {
        Ok(())
    } else {
        Err(crate::Error::InvalidPath(format!(
            "archive entry not found: {}",
            source.display_name()
        )))
    }
}

#[cfg(test)]
mod tests {
    use std::io::{self, Read};

    use crate::domain::{BuildReport, ZipSpec};

    use super::*;

    fn utf8_path(path: &std::path::Path) -> Result<&Utf8Path, io::Error> {
        Utf8Path::from_path(path).ok_or_else(|| io::Error::other("path is not UTF-8"))
    }

    fn source_file(path: &Utf8Path) -> SourceFile {
        SourceFile {
            source_root: path
                .parent()
                .map_or_else(String::new, |parent| parent.as_str().to_owned()),
            canonical_path: path.as_str().to_owned(),
            entry_name: None,
            sha1: "sha1".to_owned(),
            kind: SourceKind::BareFile,
        }
    }

    fn error_message(result: crate::Result<Vec<Utf8PathBuf>>) -> Result<String, &'static str> {
        match result {
            Ok(_) => Err("expected write plan to fail"),
            Err(error) => Ok(error.to_string()),
        }
    }

    #[test]
    fn write_plan_rejects_unsafe_output_zip_file_name() -> Result<(), Box<dyn std::error::Error>> {
        let temp_dir = tempfile::tempdir()?;
        let destination = utf8_path(temp_dir.path())?.join("output");
        let plan = BuildPlan {
            zips: vec![ZipSpec {
                file_name: "../escape.zip".to_owned(),
                entries: Vec::new(),
            }],
            report: BuildReport::default(),
            dry_run: false,
        };

        let message = error_message(write_plan(&plan, &destination))?;

        assert!(message.contains("unsafe output zip file name"));
        assert!(!destination.exists());
        Ok(())
    }

    #[test]
    fn write_plan_rejects_unsafe_zip_entry_name() -> Result<(), Box<dyn std::error::Error>> {
        let temp_dir = tempfile::tempdir()?;
        let source_path = utf8_path(temp_dir.path())?.join("source.rom");
        std::fs::write(&source_path, b"rom")?;
        let destination = utf8_path(temp_dir.path())?.join("output");
        let plan = BuildPlan {
            zips: vec![ZipSpec {
                file_name: "safe.zip".to_owned(),
                entries: vec![ZipEntrySpec {
                    output_name: "../evil.rom".to_owned(),
                    source: source_file(&source_path),
                }],
            }],
            report: BuildReport::default(),
            dry_run: false,
        };

        let message = error_message(write_plan(&plan, &destination))?;

        assert!(message.contains("unsafe zip entry name"));
        assert!(!destination.exists());
        Ok(())
    }

    #[test]
    fn write_plan_rejects_duplicate_output_zip_file_name() -> Result<(), Box<dyn std::error::Error>>
    {
        let temp_dir = tempfile::tempdir()?;
        let destination = utf8_path(temp_dir.path())?.join("output");
        let plan = BuildPlan {
            zips: vec![
                ZipSpec {
                    file_name: "safe.zip".to_owned(),
                    entries: Vec::new(),
                },
                ZipSpec {
                    file_name: "safe.zip".to_owned(),
                    entries: Vec::new(),
                },
            ],
            report: BuildReport::default(),
            dry_run: false,
        };

        let message = error_message(write_plan(&plan, &destination))?;

        assert!(message.contains("duplicate output zip file name"));
        assert!(!destination.exists());
        Ok(())
    }

    #[test]
    fn write_plan_rejects_duplicate_zip_entry_name() -> Result<(), Box<dyn std::error::Error>> {
        let temp_dir = tempfile::tempdir()?;
        let source_path = utf8_path(temp_dir.path())?.join("source.rom");
        std::fs::write(&source_path, b"rom")?;
        let destination = utf8_path(temp_dir.path())?.join("output");
        let plan = BuildPlan {
            zips: vec![ZipSpec {
                file_name: "safe.zip".to_owned(),
                entries: vec![
                    ZipEntrySpec {
                        output_name: "same.rom".to_owned(),
                        source: source_file(&source_path),
                    },
                    ZipEntrySpec {
                        output_name: "same.rom".to_owned(),
                        source: source_file(&source_path),
                    },
                ],
            }],
            report: BuildReport::default(),
            dry_run: false,
        };

        let message = error_message(write_plan(&plan, &destination))?;

        assert!(message.contains("duplicate zip entry name"));
        assert!(!destination.exists());
        Ok(())
    }

    #[test]
    fn write_plan_allows_nested_zip_entry_name() -> Result<(), Box<dyn std::error::Error>> {
        let temp_dir = tempfile::tempdir()?;
        let source_path = utf8_path(temp_dir.path())?.join("source.rom");
        std::fs::write(&source_path, b"rom")?;
        let destination = utf8_path(temp_dir.path())?.join("output");
        let plan = BuildPlan {
            zips: vec![ZipSpec {
                file_name: "safe.zip".to_owned(),
                entries: vec![ZipEntrySpec {
                    output_name: "nested/game.rom".to_owned(),
                    source: source_file(&source_path),
                }],
            }],
            report: BuildReport::default(),
            dry_run: false,
        };

        let mut written_paths = write_plan(&plan, &destination)?;
        assert_eq!(written_paths.len(), 1);
        let zip_path = written_paths.pop().ok_or("expected written zip")?;
        let mut zip = zip::ZipArchive::new(File::open(zip_path)?)?;
        let mut entry = zip.by_name("nested/game.rom")?;
        let mut contents = Vec::new();
        entry.read_to_end(&mut contents)?;

        assert_eq!(contents, b"rom");
        Ok(())
    }
}
